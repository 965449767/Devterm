    /**
     * 查询字符显示宽度（线程安全）。
     * 使用 ConcurrentHashMap.getOrPut 保证同一 codepoint 只计算一次。
     */
    fun width(codepoint: Int): Byte =
        cache.getOrPut(codepoint) { computeWidth(codepoint) }

    private fun computeWidth(codepoint: Int): Byte {
        if (codepoint < 0x20) return 0
        if (codepoint == 0x7F) return 0
        if (codepoint in highSurrogateRange) return 0
        if (isCombining(codepoint)) return 0
        if (codepoint == 0x200B || codepoint == 0x200C || codepoint == 0x200D) return 0
        if (codepoint == 0xFEFF) return 0
        return if (isWide(codepoint)) 2 else 1
    }

    private fun isCombining(cp: Int): Boolean {
        var lo = 0
        var hi = combiningRange.size / 2 - 1
        while (lo <= hi) {
            val mid = (lo + hi) / 2
            val start = combiningRange[mid * 2]
            val end = combiningRange[mid * 2 + 1]
            if (cp < start) hi = mid - 1
            else if (cp > end) lo = mid + 1
            else return true
        }
        return false
    }

    private fun isWide(codepoint: Int): Boolean {
        return when {
            codepoint >= 0x1100 && (
                codepoint <= 0x115F ||
                codepoint == 0x2329 ||
                codepoint == 0x232A ||
                codepoint in 0x2E80..0x303E ||
                codepoint in 0x3040..0x3096 ||
                codepoint in 0x3099..0x30FF ||
                codepoint in 0x3105..0x312F ||
                codepoint in 0x3131..0x318E ||
                codepoint in 0x3190..0x31E3 ||
                codepoint in 0x31F0..0x321E ||
                codepoint in 0x3220..0x3247 ||
                codepoint in 0x3250..0x4DBF ||
                codepoint in 0x4E00..0xA4CF ||
                codepoint in 0xA960..0xA97C ||
                codepoint in 0xAC00..0xD7A3 ||
                codepoint in 0xF900..0xFAFF ||
                codepoint in 0xFE10..0xFE19 ||
                codepoint in 0xFE30..0xFE6F ||
                codepoint in 0xFF01..0xFF60 ||
                codepoint in 0xFFE0..0xFFE6 ||
                codepoint in 0x1B000..0x1B0FF ||
                codepoint in 0x1B100..0x1B12F ||
                codepoint in 0x1F004..0x1F004 ||
                codepoint in 0x1F200..0x1F2FF ||
                codepoint in 0x20000..0x2FFFD ||
                codepoint in 0x30000..0x3FFFD
            ) -> true
            else -> false
        }
    }
